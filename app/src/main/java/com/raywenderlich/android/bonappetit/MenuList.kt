import androidx.compose.foundation.ExperimentalFoundationApi
import androidx.compose.foundation.background
import androidx.compose.foundation.layout.fillMaxWidth
import androidx.compose.foundation.layout.padding
import androidx.compose.foundation.lazy.LazyColumn
import androidx.compose.foundation.lazy.items
import androidx.compose.foundation.shape.CutCornerShape
import androidx.compose.material3.Text
import androidx.compose.runtime.Composable
import androidx.compose.ui.Modifier
import androidx.compose.ui.graphics.Color
import androidx.compose.ui.res.stringResource
import androidx.compose.ui.semantics.heading
import androidx.compose.ui.semantics.semantics
import androidx.compose.ui.text.font.FontWeight
import androidx.compose.ui.tooling.preview.Preview
import androidx.compose.ui.unit.dp
import androidx.compose.ui.unit.sp

@OptIn(ExperimentalFoundationApi::class)
@Composable
fun MenuList(dishes: List<Dish>, onDishSelected: (Dish) -> Unit) {
  LazyColumn {
    dishes.groupBy { it.category }.forEach { (category, dishList) ->
      item {
        Heading(category)
      }
      items(dishList) { item ->
        MenuItem(dish = item, onDishSelected = onDishSelected)
      }
    }
  }
}

@Composable
private fun Heading(category: Dish.Category) {
  Text(
    text = stringResource(category.descriptionRes),
    color = Color.White,
    fontSize = 18.sp,
    fontWeight = FontWeight.ExtraBold,
    modifier = Modifier
      .fillMaxWidth(0.75f)
      .padding(horizontal = 16.dp, vertical = 8.dp)
      .background(colorPrimary, shape = CutCornerShape(topEnd = 12.dp))
      .padding(16.dp)
      .semantics { heading() }
  )
}

